% Various 'old' Matlab tools
%
% Creation of a series of TIFF files.
%
%   tif_init    - Initialize the creation of a series of TIFF files.
%   tif_frame   - Save a frame to a TIFF file.
%
% Slow creation of an AVI file.
%
%   avi_init    - Initialize the creation of an AVI file (256 clr).
%   avi_frame   - Save a frame to AVi file.
%   avi_write   - Finish creating the AVI file.
%   avi_break   - Break creation AVI file.
%   avi_active  - Check whether an AVI file is being created.
%   comp_mrle8  - Compress using Microsoft runlength encoding (slow).
%   decomp_mrle8 - Decompress.
%
% Other tools.
%
%   clipboard   - Copy figure to clipboard. (PC only)
%   fly         - Follow a flight path.
%   collabel    - Create spreadsheet labels: A B .. Z AA AB .. AZ BA .. .
%   copy        - Copy graphics objects (see gfxmngr).
%   logicalregions - Extract boundaries of a BW image.
%   multiline   - Convert a string containing linefeeds to a char matrix.
%   paper       - Add border to paper (see md_paper).
%
% For more information contact Bert Jagers.
