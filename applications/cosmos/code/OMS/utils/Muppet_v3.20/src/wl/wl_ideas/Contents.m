% Interactive data editing and animation system.
%
%   winmngr    - Window manager.
%   wsmngr     - Workspace manager.
%   gfxmngr    - Graphics manager.
%
%   md_bitmap  - Create TIFF file.
%   md_camera  - Camera movement menu.
%   md_color   - Color selection dialog box
%                Replacement for Matlab's own uisetcolor, which
%                often crashes Matlab on SGI64.
%   md_edit    - Interface for editing data.
%   md_figzoom - Figure zoom menu.
%   md_filemem - Manager for opened files.
%   md_paper   - Add border to figure (and lock figure).
%   md_print   - Printer interface.
%   md_selectmoveresize - Replacement for selectmoveresize,
%                works also for figures with handlevisibility set
%                to off.
%
%   ui_classif - GUI for selecting thresholds and colors.
%   ui_getfile - GUI for selecting files.
%   ui_labels  - GUI for editing labels.
%   ui_message - GUI for error messages and warnings.
%   ui_type    - GUI for selecting one item.
%   ui_typeandname - GUI for selecting one item and a label.
%
%   xx_colormap - Acces for some color maps.
%
%   ideas      - GUI driven plotting tool. (beta version)
%
% For more information contact Bert Jagers.
