%% NOAA NDBC ocean
%  https://svn.oss.deltares.nl/repos/openearthrawdata/trunk/noaa/ndbc/

   %CATALOG = nc2struct('d:\checkouts\OpenEarthRawData\noaa\ndbc\ndbc_dart_locations.nc');
   [~,CATALOG,~]=noaa.dart('getcapabilities');

fews.locations_write(...
    CATALOG.geospatialCoverage_eastwest_start,...
    CATALOG.geospatialCoverage_northsouth_start,...
