    'external_name'   ,CATALOG.platform_id,...
    'display_name'    ,CATALOG.platform_name,...
    'timeIn'          ,CATALOG.timeCoverage_start,...
    'timeOut'         ,CATALOG.timeCoverage_end,...
    'locationSet_id'  ,'NOAA.ndbc.dart',...
    'locationSet_name','NOAA National Data Buoy Centre - dart program (all)',...
    'xml_Id'          ,'Id.NOAA.ndbc.dart.all.xml',...
    'xml_Locations'   ,'NOAA.ndbc.dart.Locations.xml',...
    'xml_LocationSets','NOAA.ndbc.dart.LocationSets.xml')


